piano_keys = ["a","b","c","d","e","f","g"]
print (piano_keys[2:5])
print (piano_keys[2:])
print (piano_keys[:5])
print (piano_keys[2:5:2]) # third number is the increment 
print (piano_keys[::2]) # just skipping every other one 
print (piano_keys[::-1]) # flip the list

